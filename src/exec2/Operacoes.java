public class Operacoes {

	public static void main(String[] args) {

		int adi = 5 + 2;
		// soma
		int sub = 5 - 2;
		// subtrai
		int mul = 5 * 2;
		// mutiplica
		int div = 5 / 2;
		// dividi
		int mod = 5 % 2;
		// mostra o modulo

		System.out.println("5 + 2 = " + adi + "\n" + "5 - 2 = " + sub + "\n"
				+ "5 X 2 = " + mul + "\n" + "5 / 2 = " + div + "\n"
				+ "5 % 2 = " + mod);

	}

}
